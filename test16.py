x = 20
y = 50

if x == y:                  # check if x is equal to y
    print("x is equal to y")
elif x > y:                  # check if x is greater than y
    print("x is greater than y")
elif x >= y:                # check if x is greater than or equal to y
    print("x is greater than or equal to y")
elif x <= y:        # check if x is less than or equal to y
    print("x is less than or equal to y")
elif x != y:                # check if x is not equal to y
    print("x is not equal to y")
else:               
    print("x is less than y")
    
    